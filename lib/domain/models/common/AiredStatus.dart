
/// Статусы выхода произведения
enum AiredStatus {

  /// Анонсировано
  anons,

  /// Выходит
  ongoing,

  /// Вышло
  released,

  /// Недавно вышедшее
  latest,

  ///////////////////////////////////////////////////////////////////////////
  // Дополнительные статусы для манги
  ///////////////////////////////////////////////////////////////////////////

  /// Паузы в выпуске манги
  paused,

  /// Манга перестала выпускаться
  discontinued,

  ///////////////////////////////////////////////////////////////////////////
  // Статусы MyAnimeList
  ///////////////////////////////////////////////////////////////////////////

  /// Анонсировано
  notYetAired,

  /// Выходит
  currentlyAiring,

  /// Вышло
  finishedAiring,

  ///////////////////////////////////////////////////////////////////////////
  // Статусы, если пришла любая другая строка
  ///////////////////////////////////////////////////////////////////////////

  /// Пока неизвестно
  none,

  /// Неизвестный статус (если придёт что-то не из списка)
  unknown
}